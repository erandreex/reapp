package com.reapp.reapp.Modelos;

public class ModeloGraficaDatasetData {

    private String dataRutina;
    private String dataFecha;
    private float dataCantidad;
    private int dataPosicion;

    public String getDataRutina() {
        return dataRutina;
    }

    public void setDataRutina(String dataRutina) {
        this.dataRutina = dataRutina;
    }

    public String getDataFecha() {
        return dataFecha;
    }

    public void setDataFecha(String dataFecha) {
        this.dataFecha = dataFecha;
    }

    public float getDataCantidad() {
        return dataCantidad;
    }

    public void setDataCantidad(float dataCantidad) {
        this.dataCantidad = dataCantidad;
    }

    public int getDataPosicion() {
        return dataPosicion;
    }

    public void setDataPosicion(int dataPosicion) {
        this.dataPosicion = dataPosicion;
    }

}
