package com.reapp.reapp.Modelos;

public class ModeloGraficaConsulta {

    private String idGrafica;
    private int cantRegistros;
    private String consultaFecha;

    public String getIdGrafica() {
        return idGrafica;
    }

    public void setIdGrafica(String idGrafica) {
        this.idGrafica = idGrafica;
    }

    public int getCantRegistros() {
        return cantRegistros;
    }

    public void setCantRegistros(int cantRegistros) {
        this.cantRegistros = cantRegistros;
    }

    public String getConsultaFecha() {
        return consultaFecha;
    }

    public void setConsultaFecha(String consultaFecha) {
        this.consultaFecha = consultaFecha;
    }

}
